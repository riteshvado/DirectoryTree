
class BgColors:
    """
    Class to add colors . We can use this as prefix in print statement.
    """
    BOLD = '\033[95m'
    BLUE = '\033[94m'
    CYAN = '\033[96m'
    GREEN = '\033[92m'
    YELLOW = '\033[93m'
    RED = '\033[91m'
    ENDC = '\033[0m'
    BOLD1 = '\033[1m'
    UNDERLINE = '\033[4m'
